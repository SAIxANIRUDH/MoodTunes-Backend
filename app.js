const express = require('express');
const cors = require('cors');
const songRoutes = require('./src/routes/song.routes');

const app = express();


app.use(cors());
app.use(express.json());


app.listen(5000, () => {
  console.log('Server is running on port 5000');

});
app.use('/api/songs', songRoutes);

module.exports = app;
